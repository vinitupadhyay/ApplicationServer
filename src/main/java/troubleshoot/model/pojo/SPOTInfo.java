package troubleshoot.model.pojo;

public class SPOTInfo
{
	private String UPMip = "";
	private String UPMppn = "";
	private String UPMmac = "";
	
	public SPOTInfo()
	{
		
	}

	public String getUPMip() {
		return UPMip;
	}

	public void setUPMip(String uPMip) {
		UPMip = uPMip;
	}

	public String getUPMppn() {
		return UPMppn;
	}

	public void setUPMppn(String uPMppn) {
		UPMppn = uPMppn;
	}

	public String getUPMmac() {
		return UPMmac;
	}

	public void setUPMmac(String uPMmac) {
		UPMmac = uPMmac;
	}
}
